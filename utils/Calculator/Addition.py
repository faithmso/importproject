class Addition:
    @classmethod
    def add(cls, num1, num2):
        return num1 + num2


class Subtraction:
    @classmethod
    def subtract(cls, num1, num2):
        return num1 - num2


class Multiplication:
    @classmethod
    def multiply(cls, num1, num2):
        return num1 * num2


class Division:
    @classmethod
    def divide(cls, num1, num2):
        return num1 / num2






